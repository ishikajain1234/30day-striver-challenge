/**
 * Definition for singly-linked list.
 * struct ListNode {
 *     int val;
 *     ListNode *next;
 *     ListNode() : val(0), next(nullptr) {}
 *     ListNode(int x) : val(x), next(nullptr) {}
 *     ListNode(int x, ListNode *next) : val(x), next(next) {}
 * };
 */
class Solution {
public:
    ListNode* removeNthFromEnd(ListNode* head, int n) {
        int x=0;
        ListNode* temp=head;
        while(temp!=NULL){
            x++;
            temp=temp->next;
        }
        x=x-n;
        if(x==0){
            head=head->next;
            return head;

        }
        ListNode* p=head;
        for(int i=0; i<x-1; i++){
            p=p->next;
        }
        p->next=p->next->next;
        return head;

    }
};